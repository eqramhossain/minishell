#include "utils.h"

int	ft_edge(char *str)
{
	if ((str[0] == ';' || str[0] == ':' || str[0] == '\\' || str[0] == '!')
		&& (str[1]) == '\0')
		return (ERROR);
	return (SUCCESS);
}
